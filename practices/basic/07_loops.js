// Loops
// do while, while, for

// do-while loops

let i = 1;
do {
    console.log('Value of i is :', i);
    i++;
} while (i <= 5);

//  while loops

let j = 1;
while (j <= 5) {
    console.log('Value of J is ', j);
    j++;
}

// for loops

for (let k = 0; k <= 10; k++) {
    console.log('Value of K is ', k);
}